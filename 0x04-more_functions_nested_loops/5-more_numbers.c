#include "main.h"

/**
 *  more_numbers - starting point of a function
 *
 * Description: the function prints 0 to 4 10 times
 *
 * Return: Retuns nothing
 */

void more_numbers(void)
{
	int i;
	int num;

	for (i = 0; 0 <= 10; i++)
	{
		for (num = 0; num <= 4; num++)
			_putchar(num);
	}
	_putchar('\n');
}
